# Define display_board that accepts a board and prints
# out the current state.
def display_board(places)
    puts " #{places[0]} | #{places[1]} | #{places[2]} "
    puts "-----------"
    puts " #{places[3]} | #{places[4]} | #{places[5]} "
    puts "-----------"
    puts " #{places[6]} | #{places[7]} | #{places[8]} "
end